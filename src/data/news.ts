// ヘルパー関数：IDで記事を取得
export function getNewsById(id: string): NewsItem | undefined {
  return newsData.find(item => item.id === id);
}

// ヘルパー関数：前後の記事を取得（ナビゲーション用）
export function getAdjacentNews(id: string): {
  prev: NewsItem | null;
  next: NewsItem | null;
} {
  const index = newsData.findIndex(item => item.id === id);
  
  if (index === -1) {
    return { prev: null, next: null };
  }

  return {
    prev: index > 0 ? newsData[index - 1] : null,
    next: index < newsData.length - 1 ? newsData[index + 1] : null
  };
}

