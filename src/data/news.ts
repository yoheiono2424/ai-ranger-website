// お知らせ記事の型定義
export interface NewsImage {
  src: string;          // 画像パス
  alt: string;          // 代替テキスト
  caption?: string;     // キャプション（オプション）
  position?: 'before' | 'after'; // セクションの前後どちらに配置するか
  section?: string;     // どのセクションに配置するか
}

export interface NewsItem {
  id: string;           // ユニークID（日付ベース）
  date: string;         // 表示用日付（YYYY.MM.DD）
  title: string;        // タイトル
  category: string;     // カテゴリ（プレスリリース、メディア掲載、イベント等）
  summary: string;      // 概要（一覧ページ用、1-2行）
  content: string;      // 本文（詳細ページ用）
  externalLink?: string; // 外部リンク（オプション）
  images?: NewsImage[]; // 記事内の画像（オプション）
}

